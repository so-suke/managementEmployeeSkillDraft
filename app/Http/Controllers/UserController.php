    {
        $user = User::select(
            DB::raw(
                'name_last,
                name_first,
                name_last_kana,
                name_first_kana,
                DATE_FORMAT(birth_at, "%Y年%m月%d日") as birth_at_formatted,
                YEAR(CURDATE()) - YEAR(birth_at) AS age'
            )
        )->where('id', $id)->first();
        return view('user', compact('user'));
    }

